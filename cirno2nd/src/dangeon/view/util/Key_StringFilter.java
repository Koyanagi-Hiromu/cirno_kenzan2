package dangeon.view.util;

import java.awt.Color;
import java.awt.Graphics2D;

public class Key_StringFilter {
	public final String text;
	public final int font_size;
	public final int rgb;

	public Key_StringFilter(Graphics2D g, String text) {
		this.text = text;
		font_size = g.getFont().getSize();
		Color c = g.getColor();
		rgb = c.getRGB();
	}
}
