package dangeon.model.map.field.special;

import dangeon.model.map.field.Base_Map;

public abstract class FixedMap extends Base_Map {
	/**
	 *
	 */
	private static final long serialVersionUID = 1L;

	protected FixedMap(String map_name) {
		super("specialMap", map_name);
	}

	@Override
	public boolean isEnchantedBGM_NoPlay() {
		return true;
	}

	@Override
	public boolean isHaraheru() {
		return false;
	}

	@Override
	public boolean isLightful() {
		return true;
	}

	@Override
	public boolean isMiniMapAvaible() {
		return false;
	}

}
