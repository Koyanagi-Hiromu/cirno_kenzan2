package dangeon.util;

public class Flag {
	private static boolean flag_item_plus = false;

	public static boolean isFlagItemPlus() {
		if (flag_item_plus) {
			flag_item_plus = false;
			return true;
		} else {
			return false;
		}
	}

	public static void setFlagItemPlus(boolean b) {
		flag_item_plus = b;
	}
}
